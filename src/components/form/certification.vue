}
</script>

<template>
  <div class="certificationsComponent">
    <h2 @click="toggleShow" class="toggle-header">
      <span>📜 Certifications</span>
      <span>{{ isVisible ? '▲' : '▼' }}</span>
    </h2>
    <!-- 表单内容 -->
    <div v-if="isVisible" class="form-container">
