<script setup>
import { ref } from 'vue';

// 定义认证的响应式数据
const certification = ref({
  name: '',
  issuer: '',
  date: '',
  expiryDate: '',
  url: '',
  description: '',
});

// 是否显示表单
const isVisible = ref(false);

// 切换显示/隐藏状态
